#data types
integerVar = 7
floatVar = 7.12
stringVar = "7.12"
booleanVar = True

print(type(integerVar))
print(type(floatVar))
print(type(stringVar))
print(type(booleanVar))

#typecasting
typefloat = float(stringVar)
print(typefloat, type(typefloat))
typeInt = int(typefloat)
print(typeInt, type(typeInt))